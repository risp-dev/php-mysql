CREATE TABLE `organization`(
`org_id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
`org_name` VARCHAR(80),
`org_code` INT,
`org_phone` VARCHAR(12),
`org_mail` VARCHAR(24),
`org_city` VARCHAR(24),
`org_street` VARCHAR(24),
`org_owner` VARCHAR(24)
);

CREATE TABLE `invoice`  (
`invoice_id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
`invoice_quater` INT,
`invoice_date` VARCHAR(12),
`invoice_number` VARCHAR(24) UNIQUE,
`invoice_item` VARCHAR(250),
`invoice_currency` VARCHAR(24),
`invoice_total` DECIMAL(10, 2),
`org_id` INT,
CONSTRAINT `fk_invoice_org_id` FOREIGN KEY (`org_id`) REFERENCES `organization`(`org_id`)
);

CREATE TABLE `project`  (
`project_id` INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
`project_title` VARCHAR(250),
`project_activity` VARCHAR(24),
`org_id` INT,
CONSTRAINT `fk_project_org_id` FOREIGN KEY (`org_id`) REFERENCES `organization`(`org_id`)
);

